WARN = "⚠️  WARN"

# ─────────────────────────────────────────────
# Result tracker
# ─────────────────────────────────────────────

results: dict[str, str] = {}
details: dict[str, str] = {}


def record(name: str, status: str, detail: str = "") -> None:
    """Record a test result."""
    results[name] = status
    if detail:
        details[name] = detail


# ─────────────────────────────────────────────
# 1. Supabase pipeline test
# ─────────────────────────────────────────────

def test_supabase_pipeline() -> bool:
    """
    Full Supabase round-trip:
      - Connect with service key
      - Read locations table
      - Insert a synthetic test alert
      - Read it back
      - Delete it (cleanup)
    """
    logger.info("── Supabase Pipeline ──────────────────────────")
    try:
        from supabase import create_client
        url = get_required_env("SUPABASE_URL")
        key = get_required_env("SUPABASE_SERVICE_KEY")
        sb  = create_client(url, key)

        # a) Read locations
        resp = sb.table("locations").select("id, name, lat, lon").execute()
        locations = resp.data or []
        logger.info(f"Locations in DB: {len(locations)}")
        for loc in locations[:5]:  # show first 5
            logger.info(f"  • {loc.get('name')} ({loc.get('lat')}, {loc.get('lon')})")

        # b) Insert synthetic alert
        test_alert = {
            "location_id":    None,   # no FK for test alert
            "category":       "unwetter",
            "severity":       "test",
            "title":          "[E2E TEST] Synthetic alert – bitte ignorieren",
            "description":    "Auto-generated by test_e2e.py – will be deleted immediately",
            "source_url":     "https://github.com/cryptoclemens/osint-location-monitor",
            "telegram_sent":  False,
            "raw_data":       {"e2e": True, "ts": datetime.now(timezone.utc).isoformat()},
        }
        insert_resp = sb.table("alerts").insert(test_alert).execute()
        inserted_id = insert_resp.data[0]["id"] if insert_resp.data else None

        if not inserted_id:
            record("SUPABASE_WRITE", FAIL, "Insert returned no ID")
            return False

        logger.success(f"Synthetic alert inserted – id={inserted_id}")

        # c) Read it back
        read_resp = sb.table("alerts").select("id, title").eq("id", inserted_id).execute()
        if not read_resp.data:
            record("SUPABASE_READ", FAIL, "Could not read back inserted alert")
            return False
        logger.success(f"Alert read-back OK: '{read_resp.data[0]['title'][:60]}'")

        # d) Cleanup
        sb.table("alerts").delete().eq("id", inserted_id).execute()
        verify = sb.table("alerts").select("id").eq("id", inserted_id).execute()
        if verify.data:
            record("SUPABASE_CLEANUP", WARN, "Deletion may not have worked")
        else:
            logger.success("Test alert deleted – Supabase cleanup OK")

        record("SUPABASE_PIPELINE", PASS,
               f"{len(locations)} location(s) in DB, write/read/delete cycle OK")
        return True

    except Exception as e:
        logger.error(f"Supabase pipeline failed: {e}")
        record("SUPABASE_PIPELINE", FAIL, str(e))
        return False


