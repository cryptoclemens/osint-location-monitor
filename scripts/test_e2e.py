def print_summary() -> bool:
    """Print results table and return True if all passed/warned."""
    width = 54
    logger.info("")
    logger.info("=" * width)
    logger.info(" E2E TEST RESULTS – OSInt Location Monitor v0.6.0")
    logger.info("=" * width)

    all_ok = True
    for name, status in results.items():
        detail = details.get(name, "")
        line = f"  {status}  {name}"
        if detail:
            line += f"\n          ↳ {detail}"
        logger.info(line)
        if status == FAIL:
            all_ok = False

    logger.info("=" * width)
    if all_ok:
        failed_count = sum(1 for s in results.values() if s == FAIL)
        warn_count   = sum(1 for s in results.values() if s == WARN)
        pass_count   = sum(1 for s in results.values() if s == PASS)
        skip_count   = sum(1 for s in results.values() if s == SKIP)
        logger.success(
            f"DONE  {pass_count} passed · {warn_count} warnings · "
            f"{skip_count} skipped · {failed_count} failed"
        )
    else:
        logger.error("SOME TESTS FAILED – see details above")
    logger.info("=" * width)
    return all_ok


def main() -> int:
    parser = argparse.ArgumentParser(
        description="E2E integration test for OSInt Location Monitor"
    )
    parser.add_argument(
        "--dry-run", "--skip-telegram",
        action="store_true",
        help="Run all tests but skip actual Telegram message sends",
    )
    parser.add_argument(
        "--verbose", "-v",
        action="store_true",
        help="Enable DEBUG logging",
    )
    parser.add_argument(
        "--vercel-url",
        metavar="URL",
        help="Vercel deployment URL to health-check (overrides VERCEL_URL env var)",
    )
    args = parser.parse_args()

    load_environment()
    setup_logging("DEBUG" if args.verbose else "INFO")

    logger.info("=" * 54)
    logger.info(" OSInt Location Monitor – E2E Integration Test")
    logger.info(f" Mode: {'DRY-RUN (no Telegram)' if args.dry_run else 'FULL (Telegram enabled)'}")
    logger.info(f" Test location: {E2E_TEST_LOCATION['name']}")
    logger.info("=" * 54)

    # Run all test stages
    test_supabase_pipeline()
    test_monitor_checkers()
    test_synthetic_alert(dry_run=args.dry_run)
    test_morning_report(dry_run=args.dry_run)
    test_vercel_health(base_url=args.vercel_url)

    all_ok = print_summary()
    return 0 if all_ok else 1


