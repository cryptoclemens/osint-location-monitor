# 2. Monitor: dry-run all checkers against Munich
# ─────────────────────────────────────────────

def test_monitor_checkers() -> bool:
    """
    Import monitor.py checkers and run them against Munich.
    Does NOT send Telegram messages – just verifies each checker
    returns a valid result (dict alert or None) without crashing.
    """
    logger.info("── Monitor Checkers (dry-run) ─────────────────")
    try:
        # Import checker functions from monitor.py
        import importlib.util, pathlib
        spec = importlib.util.spec_from_file_location(
            "monitor",
            pathlib.Path(__file__).parent / "monitor.py"
        )
        monitor = importlib.util.load_from_spec(spec)
        spec.loader.exec_module(monitor)

        lat = E2E_TEST_LOCATION["lat"]
        lon = E2E_TEST_LOCATION["lon"]
        passed = 0
        failed = 0

        # Weather checker
        try:
            result = monitor.check_weather(lat, lon)
            logger.success(f"check_weather → {'alert triggered' if result else 'no alert (normal)'}")
            record("CHECKER_WEATHER", PASS)
            passed += 1
        except Exception as e:
            logger.error(f"check_weather failed: {e}")
            record("CHECKER_WEATHER", FAIL, str(e))
            failed += 1

        # Earthquake checker
        try:
            result = monitor.check_earthquakes(lat, lon)
            logger.success(f"check_earthquakes → {'alert triggered' if result else 'no alert (normal)'}")
            record("CHECKER_EARTHQUAKES", PASS)
            passed += 1
        except Exception as e:
            logger.error(f"check_earthquakes failed: {e}")
            record("CHECKER_EARTHQUAKES", FAIL, str(e))
            failed += 1

        # GNews checkers (need API key)
        gnews_key = os.getenv("GNEWS_API_KEY")
        if gnews_key:
            for category in ["hochwasser", "feuer", "unruhen"]:
                try:
                    result = monitor.check_gnews(lat, lon, category, gnews_key)
                    logger.success(f"check_gnews({category}) → {'alert triggered' if result else 'no alert (normal)'}")
                    record(f"CHECKER_GNEWS_{category.upper()}", PASS)
                    passed += 1
                except Exception as e:
                    logger.error(f"check_gnews({category}) failed: {e}")
                    record(f"CHECKER_GNEWS_{category.upper()}", FAIL, str(e))
                    failed += 1
        else:
            logger.warning("GNEWS_API_KEY not set – skipping GNews checker tests")
            for cat in ["HOCHWASSER", "FEUER", "UNRUHEN"]:
                record(f"CHECKER_GNEWS_{cat}", SKIP, "No GNEWS_API_KEY")

        summary = f"{passed} passed, {failed} failed"
        record("MONITOR_CHECKERS", PASS if failed == 0 else FAIL, summary)
        logger.info(f"Monitor checkers: {summary}")
        return failed == 0

    except Exception as e:
        logger.error(f"Could not import monitor.py: {e}")
        record("MONITOR_CHECKERS", FAIL, str(e))
        return False


# ─────────────────────────────────────────────
# 3. Synthetic alert → Telegram
# ─────────────────────────────────────────────

def test_synthetic_alert(dry_run: bool = False) -> bool:
    """
    Send a synthetic alert through the Telegram pipeline.
    This mimics what monitor.py does when it detects an event.
    """
    logger.info("── Synthetic Alert → Telegram ─────────────────")
    if dry_run:
        logger.info("DRY-RUN: Telegram send skipped")
        record("SYNTHETIC_ALERT", SKIP, "dry-run mode")
        return True

    try:
        now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
        msg = (
            "🧪 *E2E-Test – Synthetischer Alert*\n\n"
            f"📍 *Ort:* {E2E_TEST_LOCATION['name']}\n"
            f"🌩️ *Kategorie:* Unwetter (Test)\n"
            f"⚡ *Schwere:* Simuliert\n\n"
            "Dies ist eine automatisch generierte Test-Nachricht "
            "vom `test_e2e.py`-Script.\n"
            "Sie bestätigt, dass der Alert-Kanal korrekt funktioniert. ✅\n\n"
            f"_Zeitstempel: {now}_"
        )
        send_telegram(msg)
        logger.success("Synthetic alert sent via Telegram ✅")
        record("SYNTHETIC_ALERT", PASS, "Telegram message delivered")
        return True
    except Exception as e:
        logger.error(f"Synthetic alert failed: {e}")
        record("SYNTHETIC_ALERT", FAIL, str(e))
        return False


