# ─────────────────────────────────────────────

def test_morning_report(dry_run: bool = False) -> bool:
    """
    Run morning_report.py against Munich and optionally send via Telegram.
    In dry-run mode: just checks that the report text is generated.
    """
    logger.info("── Morning Report ─────────────────────────────")
    try:
        import importlib.util, pathlib
        spec = importlib.util.spec_from_file_location(
            "morning_report",
            pathlib.Path(__file__).parent / "morning_report.py"
        )
        mr = importlib.util.load_from_spec(spec)
        spec.loader.exec_module(mr)

        lat = E2E_TEST_LOCATION["lat"]
        lon = E2E_TEST_LOCATION["lon"]
        name = E2E_TEST_LOCATION["name"]

        # Fetch weather data (same as morning_report does)
        weather = mr.fetch_weather(lat, lon)
        if not weather:
            record("MORNING_REPORT", FAIL, "fetch_weather returned None")
            return False

        logger.success(f"Weather fetched: {weather.get('temp', '?')}°C, "
                       f"code={weather.get('code', '?')}, "
                       f"wind={weather.get('wind', '?')} km/h")

        # Build the report message
        report_text = mr.build_report(name, weather, recent_alerts=[])
        if not report_text or len(report_text) < 50:
            record("MORNING_REPORT", FAIL, "Report text too short or empty")
            return False

        logger.success(f"Report generated ({len(report_text)} chars)")
        logger.debug(f"Report preview:\n{report_text[:200]}...")

        if dry_run:
            logger.info("DRY-RUN: Telegram send skipped")
            record("MORNING_REPORT", PASS, f"Generated {len(report_text)} chars – Telegram skipped (dry-run)")
        else:
            # Prefix with test indicator
            test_report = f"🧪 *E2E-Test – Morgenbericht*\n\n{report_text}"
            send_telegram(test_report)
            logger.success("Morning report sent via Telegram ✅")
            record("MORNING_REPORT", PASS, f"Generated + sent ({len(report_text)} chars)")

        return True

    except AttributeError as e:
        # morning_report.py might not expose fetch_weather/build_report
        logger.warning(f"morning_report.py API mismatch: {e}")
        logger.info("Falling back to full morning_report run")
        return _test_morning_report_full(dry_run)
    except Exception as e:
        logger.error(f"Morning report test failed: {e}")
        record("MORNING_REPORT", FAIL, str(e))
        return False


def _test_morning_report_full(dry_run: bool) -> bool:
    """Fallback: run morning_report as a subprocess to verify it completes."""
    import subprocess, sys as _sys
    env = os.environ.copy()
    if dry_run:
        env["MORNING_REPORT_DRY_RUN"] = "1"

    result = subprocess.run(
        [_sys.executable, "scripts/morning_report.py"],
        capture_output=True, text=True, env=env, timeout=60
    )
    if result.returncode == 0:
        logger.success("morning_report.py exited 0 ✅")
        record("MORNING_REPORT", PASS, "subprocess exit 0")
        return True
    else:
        logger.error(f"morning_report.py exited {result.returncode}")
        logger.error(result.stderr[-500:] if result.stderr else "(no stderr)")
        record("MORNING_REPORT", FAIL, f"exit {result.returncode}")
        return False


