# 5. Vercel health check (if deployed)
# ─────────────────────────────────────────────

def test_vercel_health(base_url: Optional[str] = None) -> bool:
    """
    If VERCEL_URL env var is set, check that the deployed app
    returns HTTP 200 on the login page (all other pages redirect there).
    """
    logger.info("── Vercel Deployment Health ───────────────────")
    url = base_url or os.getenv("VERCEL_URL")
    if not url:
        logger.info("VERCEL_URL not set – skipping deployment health check")
        record("VERCEL_HEALTH", SKIP, "Set VERCEL_URL=https://your-app.vercel.app to enable")
        return True

    # Ensure https prefix
    if not url.startswith("http"):
        url = f"https://{url}"

    try:
        resp = requests.get(f"{url}/login", timeout=15, allow_redirects=True)
        if resp.status_code == 200:
            logger.success(f"Vercel health OK – {url}/login → 200")
            record("VERCEL_HEALTH", PASS, f"HTTP 200 from {url}/login")
            return True
        else:
            logger.warning(f"Unexpected status {resp.status_code} from {url}/login")
            record("VERCEL_HEALTH", WARN, f"HTTP {resp.status_code}")
            return False
    except requests.RequestException as e:
        logger.error(f"Vercel health check failed: {e}")
        record("VERCEL_HEALTH", FAIL, str(e))
        return False


